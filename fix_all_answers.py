# Load
with open('/tmp/np-anesthesia/questions.json') as f:
    qs = json.load(f)

# Stats
total_checked = 0
total_corrected = 0
total_explanations_removed = 0
report = []

for q in qs:
    key = (q['year'], q['specialty'])
    if key not in OFFICIAL:
        continue
    qnum = int(re.search(r'-(?:T|A)(\d+)$', q['id']).group(1))
    official = OFFICIAL[key].get(qnum)
    if not official:
        continue
    total_checked += 1
    current = q['answer']
    if current != official:
        total_corrected += 1
        report.append((q['id'], current, official))
        # Update answer
        q['answer'] = official
        # Re-derive answerText (use first letter if multi)
        primary = official[0] if official != '*' else ''
        if primary:
            for o in q['options']:
                if o['key'] == primary:
                    q['answerText'] = o['text']
                    break
            else:
                q['answerText'] = official
        else:
            q['answerText'] = "送分（公告為送分題）"
        # REMOVE detailed explanation since it was based on wrong answer
        if q.get('explanation') and isinstance(q['explanation'], dict) and q['explanation'].get('mechanism'):
            del q['explanation']
            total_explanations_removed += 1

# Report
print(f"=== Answer Correction Report ===")
print(f"Total questions checked: {total_checked}")
print(f"Answers corrected: {total_corrected}")
print(f"Detailed explanations removed (built on wrong answers): {total_explanations_removed}")
print()
print("=== Per-paper breakdown ===")
from collections import Counter
by_paper = Counter()
for qid, _, _ in report:
    by_paper[qid.rsplit('-', 1)[0]] += 1
for paper, count in sorted(by_paper.items()):
    print(f"  {paper}: {count} corrections")

# Save
with open('/tmp/np-anesthesia/questions.json', 'w') as f:
    json.dump(qs, f, ensure_ascii=False, separators=(',', ':'))

